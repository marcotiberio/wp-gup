            Indeed, Poland looks extraterrestrial when seen from the viewpoint of a bird – especially in winter, when the country is covered in snow. Just as the Rorschach test allows for subjective perceptions of inkblots so we assign a conventional meaning to a symbol (for example, we all agree that a small triangle pointing to the right will start a video or an audio file) the urge to ‘bend’ these visual abstractions towards the known is strong, while nevertheless leaving the onlooker in a state of oblivion.
